    private void botaoRemovaerFerramenta1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemovaerFerramenta1ActionPerformed
        
        if(tabelaDeFerramentas.getSelectedRow() != -1){
            
            Ferramenta ferramenta = new Ferramenta();
            FerramentaDAO dao = new FerramentaDAO();
            
            ferramenta.setId((int)tabelaDeFerramentas.getValueAt(tabelaDeFerramentas.getSelectedRow(), 0));
            dao.Delete(ferramenta);
            
            campoTextoFerramenta.setText(null);
            campoTextoMarca.setText(null);
            campoTextoPreco.setText(null);
            
            readJTable();
        }else{
            
            JOptionPane.showMessageDialog(null, "Selecione algo para excluir!");
        }
        
    }//GEN-LAST:event_botaoRemovaerFerramenta1ActionPerformed

    private void tabelaDeFerramentasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaDeFerramentasMouseClicked
        if(tabelaDeFerramentas.getSelectedRow() != -1){
            
            campoTextoFerramenta.setText(tabelaDeFerramentas.getValueAt(tabelaDeFerramentas.getSelectedRow(), 1).toString());
            campoTextoMarca.setText(tabelaDeFerramentas.getValueAt(tabelaDeFerramentas.getSelectedRow(), 2).toString());
            campoTextoPreco.setText(tabelaDeFerramentas.getValueAt(tabelaDeFerramentas.getSelectedRow(), 3).toString());
            
        }
    }//GEN-LAST:event_tabelaDeFerramentasMouseClicked

    private void botaoAtualizarFerramentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarFerramentaActionPerformed
        
        if(tabelaDeFerramentas.getSelectedRow() != -1){
            
            if(!campoTextoFerramenta.getText().trim().isEmpty()){
            
            Ferramenta ferramenta = new Ferramenta();
          FerramentaDAO dao = new FerramentaDAO();
          
          ferramenta.setNome(campoTextoFerramenta.getText());
          ferramenta.setMarca(campoTextoMarca.getText());
          try{
              ferramenta.setPreco(Double.parseDouble(campoTextoPreco.getText()));
              ferramenta.setId((int)tabelaDeFerramentas.getValueAt(tabelaDeFerramentas.getSelectedRow(), 0));
              dao.Update(ferramenta);
              
              campoTextoFerramenta.setText(null);
            campoTextoMarca.setText(null);
            campoTextoPreco.setText(null);
              
              tabelaDeFerramentas.clearSelection();
          }catch(NumberFormatException ex){
              JOptionPane.showMessageDialog(null, "Informe o preço corretamente!");
          }
          readJTable();
            
   
            }else{
                
                JOptionPane.showMessageDialog(null, "Coloque o nome da ferramenta!", "Erro", JOptionPane.ERROR_MESSAGE);
            
            }
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Selecione algo que queira atualizar!");
        }
       
           
            
        
    }//GEN-LAST:event_botaoAtualizarFerramentaActionPerformed

