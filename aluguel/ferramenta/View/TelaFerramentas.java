    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaDeFerramentas.getModel();
        modelo.setNumRows(0);
        FerramentaDAO dao = new FerramentaDAO();
        
        for (Ferramenta a: dao.Read()) {
            
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getMarca(),
                a.getPreco()
            });
            
        }
    }

